// Violation heatmap (hour x zone for manager view)
export const VIOLATION_HEATMAP: { hour: number; zone: string; value: number }[] = [];
const ZONES_HM = ["Zone A", "Zone B", "Zone C", "Zone D", "Zone E", "Zone F"];
// Use deterministic values instead of Math.random()
const HEATMAP_SEED = [3, 7, 2, 9, 1, 5, 4, 8, 6, 2, 7, 3, 5, 1, 9, 4, 8];
let seedIdx = 0;
for (const zone of ZONES_HM) {
  for (let h = 6; h <= 22; h++) {
    const isRisky = zone === "Zone C" || zone === "Zone E";
    const isPeak  = h >= 13 && h <= 16;
    const noise   = HEATMAP_SEED[seedIdx++ % HEATMAP_SEED.length];
    VIOLATION_HEATMAP.push({
      hour:  h,
      zone,
      value: Math.round((isRisky ? 15 : 3) * (isPeak ? 2.5 : 1) + noise * 0.5),
    });
  }
}

